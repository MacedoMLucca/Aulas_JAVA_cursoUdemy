package entities;

public class Product153 {
	
	private String name;
	private Double price;
	
//	CONSTRUTORES
	public Product153() {
	}
	public Product153(String name, Double price) {
		this.name = name;
		this.price = price;
	}
	
//	GET e SET 
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
//	----------
	public Double getPrice() {
		return price;
	}
	public void setPrice(Double price) {
		this.price = price;
	}
	
	
}
